                <h1>Fire Alarm Unit Description</h1>
                <p style="font-size:16px"><span>R-type fire alarm control panel (stand-alone type)</span>
                <br>It is installed in big buildings to monitor and control fire prevention situation real-time. For convenience, all 
                  situations are shown on the screen in images and text.<br><br>

                  <span>R-type fire alarm control panel (LED type)</span>
                  <br>It is installed in small buildings to monitor and control fire prevention situation real-time. R-type system was
                  adopted with low cost, and for convenience, all situations are shown on screen in images and text.<br><br>
                  
                  <span>Integrated fire monitoring section (control desk)</span>
                  <br>It is installed by an R-type combine fire alarm control panel to quickly response to fire. In case of fire, the 
                  location can be quickly and accurately verified on the distribution diagram and the floor plan on the screen.
                  It allows for early fire extinguishing and escape. Several R-type fire alarm control panels are connected 
                  through a network and managed with an integrated management <br><br>
                  
                  <span>P-type fire alarm control panel</span>
                  <br>It is installed to monitor and control fire prevention situation. All situations (fire, alarm) can be monitored
                  with lights on the LED display window<br><br>
                  
                  <span>P-type fire alarm control panel(fire alarm control panel for linear heat detector)</span>
                  <br>When a signal is received from a linear heat detector, a calculated distance is shown and alarm goes off.<br><br>
                  
                  <span>Transponder</span>
                  <br>It expands the transponder capacity of an R-type compound fire alarm control panel. It is installed in small 
                  buildings inside a large complex. Several transponders are installed and they can be monitored and 
                  controlled with the main fire alarm control panel. <br><br>
                  
                  <span>Repeater</span>
                  <br>It converts signals from detector, alarm, and fire extinguishing system and sends to an R-type fire alarm 
                  control panel. It sends signals from the R-type fire alarm control panel to operate alarm and fire
                  extinguishing system.<br><br>
                  
                  <span>Rate of rise heat detector</span>
                  <br>It operates when it detects a sudden temperature change in the assigned area.<br><br>
                  
                  <span>Fixed temperature heat detector</span>
                  <br>It operates when the temperature inside the area exceeds the specified temperature. It is usually installed in 
                  places where fire is regularly used such as kitchen.<br><br>
                  
                  <span>Photo electric smoke detector</span>
                  <br>It operates when a space is filled with smoke that exceeds specified concentration. It is installed in living 
                  rooms and aisles.<br><br> 
                  
                  <span>Analogue Photo electric smoke detector</span>
                  <br>It operates when a space is filled with smoke that exceeds specified concentration. The concentration can 
                  be adjusted and each detector is given an address, so it can be recognized easily in case of fire.<br><br> 
                  
                  <span>Analogue type heat detector</span>
                  <br>It operates when the temperature inside the area exceeds the specified temperature. The temperature can 
                  be adjusted and each detector is given an address, so it can be recognized easily when the place is set on fire<br><br>
                  
                  <span>Linear heat detector</span>
                  <br>It operates when the temperature in the area exceeds the specified temperature. It is installed in tray and 
                  pipe utility conduit.<br><br>
                  
                  <span>Emergency exit light</span>
                  <br>It guides people to escape through emergency exit. Size varies: large, medium, and small. And high 
                  efficiency LED is used as light source<br><br>
                  
                  <span>Aisle exit light</span>
                  <br>It guides people to escape through emergency exit. Type varies: room pathway, aisle, and stairway. And 
                  high efficiency LED is used as light source.<br><br> 
                  
                  <span>Compound emergency exit light</span>
                  <br>It guides people to escape through emergency exit. To help hearing-impaired and visually impaired people, 
                  the strobe and sound systemwere added.<br><br> 
                  
                  <span>Auxiliary power panel</span>
                  <br>It supplies power to Repeaters and linked systems. It includes terminal strips and a power panel.<br><br> 
                  
                  <span>Annunciator panel</span>
                  <br>It monitors flame zone inside a large complex with lights on the LED display window<br><br>
                  
                  <span>Manual call point</span>
                  <br>A person who found fire manually presses the button to set off alarm and send a signal to fire alarm control 
                  panel.<br><br> 
                  
                  <span>Alarm bell</span>
                  <br>It informs people with an alarm sound inside the building that there is fire to help escape.<br><br> 
                  
                  <span>Indication lamp</span>
                  <br>It shows where a fire alarm is located. LED is used as light source.<br><br> 
                  
                  <span>Manual station</span>
                  <br>In case of fire, water system, gas system, and smoke control systemare manually controlled and operated. 
                  It is categorized into SVP(SPRINKLER) manual station, damper manual station, and extinguishing agent 
                  manual station. <br><br>
                  
                  <span>Electronic siren</span>
                  <br>It alarms people inside the building with sound when water system, gas system, and smoke control system
                  are operated. <br><br>
                  
                  <span>Door release</span>
                  <br>In case of fire, a fire door is automatically locked. It is used for fire preventive purposes.<br><br>
                  
                  <span>Emergency receptacle</span>
                  <br>It is a necessary device in fire extinguishing activity. Emergency power is supplied in single phase 220V and 
                  three phase 380V. <br><br>
                  
                  <span>Discharge indicating lamp</span>
                  <br>It informs the emission of extinguishing agent.<br><br> 
                  
                  <span>Strobe</span>
                  <br>It notifies hearing impaired people of fire through flickering light. Xenon lamp and LED lamp is used as light
                  source<br><br>
                  
                  <span>Foot light</span>
                  <br>It is used in a dark space such as an auditorium to guide people to exit through the emergency exit. High 
                  efficiency LED is use as light source.
                </p>
